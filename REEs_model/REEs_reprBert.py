    def encoder(self, rees_input):
        '''
        :param rees: batch_size of one "p_1 ^ p_2 ... ^ p_m -> p_0"
        :return:
        '''

        rees_input_lhs = rees_input[:, :MAX_LHS_PREDICATES]
        print("The shape of ree : ", rees_input_lhs.shape)
        rees_input_rhs = rees_input[:, MAX_LHS_PREDICATES]

        # consider the first predicate of LHS
        encoder_input_one = self.preprocessing_layer(rees_input_lhs[:, 0])
        output_one = self.encoder_one(encoder_input_one)
        bert_embed = output_one['pooled_output']
        # condider the remaining predicates of LHS
        for i in range(1, MAX_LHS_PREDICATES, 1):
            encoder_input_one = self.preprocessing_layer(rees_input_lhs[:, i])
            output_one = self.encoder_one(encoder_input_one)
            bert_embed += output_one['pooled_output']
        bert_embed /= MAX_LHS_PREDICATES
        # consider the RHS
        encoder_input_one = self.preprocessing_layer(rees_input_rhs)
        output_one = self.encoder_one(encoder_input_one)
        bert_embed = tf.keras.layers.Concatenate(axis=1)([bert_embed, output_one['pooled_output']])
        #bert_embed = tf.keras.layers.Dropout(0.1)( bert_embed)
        return bert_embed

